print("----------------List data-type-----------------")
my_list = ["Devashish",10,20,40,30,10.5,"Dev123"]
print(my_list)
print(type(my_list))

print("----------------Tuple data-type-----------------")
my_tuple = ("Devashish",10,20,40,30,10.5,"Dev123")
print(my_tuple)
print(type(my_tuple))

print("----------------Range data-type-----------------")
my_range = range(10)
print(list(my_range))
print(tuple(my_range))
print(type(my_range))
print(type(list(my_range)))
print(type(tuple(my_range)))
